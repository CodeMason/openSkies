package com.github.skittishSloth.openSkies.engine.inventory.items;

/**
 *
 * @author mcory01
 */
public class ItemDetails {

    public enum Type {
        QUEST,
        SPECIAL,
        NORMAL;
    }

    public ItemDetails() {
    }

    public String getName() {
        return name;
    }

    public void setName(final String name) {
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(final int id) {
        this.id = id;
    }

    public Type getType() {
        return type;
    }

    public void setType(final Type type) {
        this.type = type;
    }

    private String name;
    private int id;
    private Type type;
}
